function buildNpcTrustLookup(): NpcTrustLookup {
    if (!loadGameRules().enableNpcRegistry) {
        return { readTrust: () => readNpcPlayerTrust(undefined) };
    }
    const registry = loadNpcRegistry();
    return {
        readTrust(npcId: string) {
            return readNpcPlayerTrust(registry.npcs[npcId]?.disposition?.playerTrust);
        },
    };
}

